# 복수 컬럼 중복 조회
SELECT
    USER_ID
,   PRODUCT_ID
#,   COUNT(*) AS DUP_PUCHASE_CNT
FROM
    ONLINE_SALE
GROUP BY
    USER_ID, PRODUCT_ID
HAVING
    COUNT(*) > 1
ORDER BY
    USER_ID ASC
,   PRODUCT_ID DESC;

